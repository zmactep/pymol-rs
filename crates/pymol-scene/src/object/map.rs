//! Map object for electron density maps
//!
//! Provides storage and rendering of volumetric density data
//! with isomesh and isosurface visualization.

use lin_alg::f32::Vec3;
use pymol_mol::Symmetry;
use pymol_render::{Grid3D, MeshRep, MeshVertex, RenderContext, Representation};
use pymol_settings::GlobalSettings;

use super::{Object, ObjectState, ObjectType};

/// Data for a single map state
#[derive(Debug, Clone)]
pub struct MapData {
    /// 3D grid storing density values
    pub grid: Grid3D,
    /// Symmetry information (if crystallographic)
    pub symmetry: Option<Symmetry>,
}

impl MapData {
    /// Create a new map data from a grid
    pub fn new(grid: Grid3D) -> Self {
        Self {
            grid,
            symmetry: None,
        }
    }

    /// Create map data with symmetry
    pub fn with_symmetry(grid: Grid3D, symmetry: Symmetry) -> Self {
        Self {
            grid,
            symmetry: Some(symmetry),
        }
    }

    /// Get the bounding box of this map
    pub fn bounds(&self) -> ([f32; 3], [f32; 3]) {
        let vd = self.grid.vertex_dims();
        let min = self.grid.origin;
        let max = [
            min[0] + (vd[0] - 1) as f32 * self.grid.spacing,
            min[1] + (vd[1] - 1) as f32 * self.grid.spacing,
            min[2] + (vd[2] - 1) as f32 * self.grid.spacing,
        ];
        (min, max)
    }
}

/// Display mode for map objects
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum MapDisplayMode {
    /// Wireframe mesh at isovalue
    #[default]
    Isomesh,
    /// Solid surface at isovalue
    Isosurface,
    /// Volume rendering (not yet implemented)
    Volume,
}

/// An electron density map object
///
/// Stores 3D volumetric data and renders it as isomesh or isosurface.
/// Supports multiple states (e.g., for time-series maps).
pub struct MapObject {
    /// Map name
    name: String,
    /// Visual state (enabled, color, etc.)
    state: ObjectState,
    /// Map data for each state
    states: Vec<MapData>,
    /// Current display state index
    current_state: usize,
    /// Isocontour level (sigma or absolute)
    level: f32,
    /// Display mode (mesh, surface, volume)
    display_mode: MapDisplayMode,
    /// Mesh color (RGBA)
    mesh_color: [f32; 4],
    /// Carve around selection radius (0 = no carve)
    carve_radius: f32,
    /// Per-object settings
    settings: Option<GlobalSettings>,
    /// Cached mesh representation
    cached_mesh: Option<MeshRep>,
    /// Whether the cached data needs rebuilding
    dirty: bool,
}

impl MapObject {
    /// Create a new map object from grid data
    pub fn new(name: &str, grid: Grid3D) -> Self {
        Self {
            name: name.to_string(),
            state: ObjectState::default(),
            states: vec![MapData::new(grid)],
            current_state: 0,
            level: 1.0,
            display_mode: MapDisplayMode::default(),
            mesh_color: [0.0, 0.5, 1.0, 1.0], // Blue
            carve_radius: 0.0,
            settings: None,
            cached_mesh: None,
            dirty: true,
        }
    }

    /// Create a map object with map data
    pub fn from_map_data(name: &str, data: MapData) -> Self {
        Self {
            name: name.to_string(),
            state: ObjectState::default(),
            states: vec![data],
            current_state: 0,
            level: 1.0,
            display_mode: MapDisplayMode::default(),
            mesh_color: [0.0, 0.5, 1.0, 1.0],
            carve_radius: 0.0,
            settings: None,
            cached_mesh: None,
            dirty: true,
        }
    }

    /// Create an empty map object
    pub fn empty(name: &str) -> Self {
        Self {
            name: name.to_string(),
            state: ObjectState::default(),
            states: Vec::new(),
            current_state: 0,
            level: 1.0,
            display_mode: MapDisplayMode::default(),
            mesh_color: [0.0, 0.5, 1.0, 1.0],
            carve_radius: 0.0,
            settings: None,
            cached_mesh: None,
            dirty: true,
        }
    }

    /// Add a state to the map
    pub fn add_state(&mut self, data: MapData) {
        self.states.push(data);
        self.dirty = true;
    }

    /// Get the current map data
    pub fn current_data(&self) -> Option<&MapData> {
        self.states.get(self.current_state)
    }

    /// Get mutable access to current map data
    pub fn current_data_mut(&mut self) -> Option<&mut MapData> {
        self.dirty = true;
        self.states.get_mut(self.current_state)
    }

    /// Get the grid for the current state
    pub fn grid(&self) -> Option<&Grid3D> {
        self.states.get(self.current_state).map(|d| &d.grid)
    }

    /// Get mutable access to the grid (marks dirty)
    pub fn grid_mut(&mut self) -> Option<&mut Grid3D> {
        self.dirty = true;
        self.states.get_mut(self.current_state).map(|d| &mut d.grid)
    }

    /// Get the isocontour level
    pub fn level(&self) -> f32 {
        self.level
    }

    /// Set the isocontour level
    pub fn set_level(&mut self, level: f32) {
        if (self.level - level).abs() > 1e-6 {
            self.level = level;
            self.dirty = true;
        }
    }

    /// Get the display mode
    pub fn display_mode(&self) -> MapDisplayMode {
        self.display_mode
    }

    /// Set the display mode
    pub fn set_display_mode(&mut self, mode: MapDisplayMode) {
        if self.display_mode != mode {
            self.display_mode = mode;
            self.dirty = true;
        }
    }

    /// Get the mesh color
    pub fn mesh_color(&self) -> [f32; 4] {
        self.mesh_color
    }

    /// Set the mesh color
    pub fn set_mesh_color(&mut self, color: [f32; 4]) {
        self.mesh_color = color;
        self.dirty = true;
    }

    /// Get the carve radius
    pub fn carve_radius(&self) -> f32 {
        self.carve_radius
    }

    /// Set the carve radius
    pub fn set_carve_radius(&mut self, radius: f32) {
        if (self.carve_radius - radius).abs() > 1e-6 {
            self.carve_radius = radius;
            self.dirty = true;
        }
    }

    /// Check if dirty
    pub fn is_dirty(&self) -> bool {
        self.dirty
    }

    /// Mark as dirty
    pub fn invalidate(&mut self) {
        self.dirty = true;
    }

    /// Check if the map has any data
    pub fn is_empty(&self) -> bool {
        self.states.is_empty()
    }

    /// Build the mesh from the grid using marching cubes
    fn build_mesh(&mut self) {
        // Extract isosurface first (needs immutable borrow of grid)
        let result = match self.states.get(self.current_state) {
            Some(data) => {
                Self::extract_isosurface(&data.grid, self.level, self.mesh_color)
            }
            None => {
                (Vec::new(), Vec::new())
            }
        };

        // Now set the mesh (mutable borrow)
        let mesh = self.cached_mesh.get_or_insert_with(MeshRep::new);
        mesh.set_mesh(result.0, result.1);
        self.dirty = false;
    }

    /// Simple marching cubes implementation for isosurface extraction
    fn extract_isosurface(grid: &Grid3D, level: f32, color: [f32; 4]) -> (Vec<MeshVertex>, Vec<u32>) {
        let mut vertices = Vec::new();
        let mut indices = Vec::new();

        let dims = grid.dims;

        // Process each cell
        for z in 0..dims[2] {
            for y in 0..dims[1] {
                for x in 0..dims[0] {
                    let cube_values = grid.cube_values(x, y, z);
                    let cube_positions = grid.cube_positions(x, y, z);

                    // Calculate cube index based on which corners are inside
                    let mut cube_index = 0u8;
                    for i in 0..8 {
                        if cube_values[i] < level {
                            cube_index |= 1 << i;
                        }
                    }

                    // Skip if cube is entirely inside or outside
                    if cube_index == 0 || cube_index == 255 {
                        continue;
                    }

                    // Get edges that are crossed
                    let edge_table = EDGE_TABLE[cube_index as usize];
                    if edge_table == 0 {
                        continue;
                    }

                    // Interpolate vertex positions on edges
                    let mut edge_vertices = [[0.0f32; 3]; 12];
                    let mut edge_normals = [[0.0f32; 3]; 12];

                    for i in 0..12 {
                        if edge_table & (1 << i) != 0 {
                            let (v1, v2) = EDGE_VERTICES[i];
                            let p1 = cube_positions[v1];
                            let p2 = cube_positions[v2];
                            let val1 = cube_values[v1];
                            let val2 = cube_values[v2];

                            // Linear interpolation
                            let t = if (val2 - val1).abs() > 1e-6 {
                                (level - val1) / (val2 - val1)
                            } else {
                                0.5
                            };

                            edge_vertices[i] = [
                                p1[0] + t * (p2[0] - p1[0]),
                                p1[1] + t * (p2[1] - p1[1]),
                                p1[2] + t * (p2[2] - p1[2]),
                            ];

                            // Compute gradient as normal (would need neighboring cells for proper gradient)
                            edge_normals[i] = Self::compute_gradient(grid, edge_vertices[i], grid.spacing);
                        }
                    }

                    // Generate triangles
                    let tri_table = &TRI_TABLE[cube_index as usize];
                    let mut i = 0;
                    while tri_table[i] != -1 {
                        let base_idx = vertices.len() as u32;

                        let e0 = tri_table[i] as usize;
                        let e1 = tri_table[i + 1] as usize;
                        let e2 = tri_table[i + 2] as usize;

                        vertices.push(MeshVertex {
                            position: edge_vertices[e0],
                            normal: edge_normals[e0],
                            color,
                        });
                        vertices.push(MeshVertex {
                            position: edge_vertices[e1],
                            normal: edge_normals[e1],
                            color,
                        });
                        vertices.push(MeshVertex {
                            position: edge_vertices[e2],
                            normal: edge_normals[e2],
                            color,
                        });

                        indices.push(base_idx);
                        indices.push(base_idx + 1);
                        indices.push(base_idx + 2);

                        i += 3;
                    }
                }
            }
        }

        (vertices, indices)
    }

    /// Compute gradient (normal) at a point using central differences
    fn compute_gradient(grid: &Grid3D, pos: [f32; 3], spacing: f32) -> [f32; 3] {
        let h = spacing * 0.5;

        // Sample the grid (simplified - would use trilinear interpolation)
        let sample = |p: [f32; 3]| -> f32 {
            let ix = ((p[0] - grid.origin[0]) / grid.spacing) as usize;
            let iy = ((p[1] - grid.origin[1]) / grid.spacing) as usize;
            let iz = ((p[2] - grid.origin[2]) / grid.spacing) as usize;
            let vd = grid.vertex_dims();
            if ix < vd[0] && iy < vd[1] && iz < vd[2] {
                grid.get(ix, iy, iz)
            } else {
                0.0
            }
        };

        let gx = sample([pos[0] + h, pos[1], pos[2]]) - sample([pos[0] - h, pos[1], pos[2]]);
        let gy = sample([pos[0], pos[1] + h, pos[2]]) - sample([pos[0], pos[1] - h, pos[2]]);
        let gz = sample([pos[0], pos[1], pos[2] + h]) - sample([pos[0], pos[1], pos[2] - h]);

        // Normalize
        let len = (gx * gx + gy * gy + gz * gz).sqrt();
        if len > 1e-6 {
            [-gx / len, -gy / len, -gz / len] // Negate for outward normal
        } else {
            [0.0, 0.0, 1.0]
        }
    }

    /// Prepare for rendering (build mesh and upload to GPU)
    pub fn prepare_render(&mut self, context: &RenderContext) {
        if !self.dirty {
            return;
        }

        self.build_mesh();

        if let Some(ref mut mesh) = self.cached_mesh {
            mesh.upload(context.device(), context.queue());
        }
    }

    /// Render the map
    pub fn render<'a>(&'a self, render_pass: &mut wgpu::RenderPass<'a>, context: &'a RenderContext) {
        if !self.state.enabled || self.is_empty() {
            return;
        }

        if let Some(ref mesh) = self.cached_mesh {
            if !mesh.is_empty() {
                let pipeline = context.mesh_pipeline(pymol_render::pipeline::BlendMode::Opaque);
                render_pass.set_pipeline(&pipeline);
                render_pass.set_bind_group(0, context.uniform_bind_group(), &[]);
                mesh.render(render_pass);
            }
        }
    }

    /// Compute the bounding box
    fn compute_extent(&self) -> Option<(Vec3, Vec3)> {
        let data = self.states.get(self.current_state)?;
        let (min, max) = data.bounds();
        Some((
            Vec3::new(min[0], min[1], min[2]),
            Vec3::new(max[0], max[1], max[2]),
        ))
    }
}

impl Object for MapObject {
    fn name(&self) -> &str {
        &self.name
    }

    fn object_type(&self) -> ObjectType {
        ObjectType::Map
    }

    fn state(&self) -> &ObjectState {
        &self.state
    }

    fn state_mut(&mut self) -> &mut ObjectState {
        &mut self.state
    }

    fn extent(&self) -> Option<(Vec3, Vec3)> {
        self.compute_extent()
    }

    fn n_states(&self) -> usize {
        self.states.len().max(1)
    }

    fn current_state(&self) -> usize {
        self.current_state
    }

    fn set_current_state(&mut self, state: usize) -> bool {
        if state < self.states.len() {
            if self.current_state != state {
                self.current_state = state;
                self.dirty = true;
            }
            true
        } else {
            false
        }
    }

    fn settings(&self) -> Option<&GlobalSettings> {
        self.settings.as_ref()
    }

    fn settings_mut(&mut self) -> Option<&mut GlobalSettings> {
        self.settings.as_mut()
    }

    fn set_name(&mut self, name: String) {
        self.name = name;
    }
}

