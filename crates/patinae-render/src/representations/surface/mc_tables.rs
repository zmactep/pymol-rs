//! Marching-cubes lookup tables for surface extraction.
//!
//! Three canonical Bourke tables drive the GPU MC dispatch and the CPU
//! reference oracle in `surface::oracle`:
//!
//! - [`EDGE_TABLE`] — 256-entry bitmask. Bit `e` set ⇔ edge `e` of the cube
//!   is crossed by the isosurface for the corresponding case index.
//! - [`TRI_TABLE`] — 256×16 of edge IDs, packed with `-1` terminators. Each
//!   case lists up to 5 triangles (15 edges) in canonical winding order.
//! - [`EDGE_VERTICES`] — 12×2 of corner IDs. Edge `e` runs from corner
//!   `EDGE_VERTICES[e][0]` to corner `EDGE_VERTICES[e][1]`.
//!
//! Cube corner numbering (left-handed, +X right, +Y up, +Z forward):
//!
//! ```text
//!         4 -------- 5         z=1 layer
//!        /|         /|
//!       7 -------- 6 |
//!       | 0 -------|-1         z=0 layer (front)
//!       |/         |/
//!       3 -------- 2
//!       y↑     x→
//! ```
//!
//! Edge numbering: 0..3 are bottom-layer (z=0), 4..7 top-layer (z=1), 8..11
//! are vertical posts. See [`EDGE_VERTICES`] for explicit endpoints.
//!
//! ## GPU upload
//!
//! WGSL storage buffers are 32-bit-element addressable; we don't have native
//! `i8`/`u16` storage. [`pack_for_gpu`] returns a flat `Vec<u32>` laid out as
//! described in [`GpuMcTablesLayout`] so the shader can index directly.
//!
//! Source: Paul Bourke's classic tables (public domain).
//!
//! ## Winding convention
//!
//! Bourke's tables are written assuming `bit=1 ⇔ corner_value < iso`. We use
//! the inverted convention `bit=1 ⇔ corner_value >= iso` (matches the WGSL
//! classify pass and the CPU oracle), so when emitting a triangle from
//! `TRI_TABLE[case][3*k .. 3*k+3]` you must read the three edge slots in
//! reverse (`2-k_local`) — otherwise winding flips and back-face culling eats
//! the surface.

