    #[test]
    fn empty_cases_have_no_triangles() {
        // case 0 (all corners below iso) and case 255 (all above) — both
        // should produce no triangles.
        assert_eq!(TRI_COUNT[0], 0);
        assert_eq!(TRI_COUNT[255], 0);
        assert!(TRI_TABLE[0].iter().all(|&v| v == -1));
        assert!(TRI_TABLE[255].iter().all(|&v| v == -1));
        assert_eq!(EDGE_TABLE[0], 0);
        assert_eq!(EDGE_TABLE[255], 0);
    }

    #[test]
    fn tri_count_matches_terminator() {
        // Each TRI_TABLE row must be a sequence of triangles (3 edges) ended
        // by `-1`. Verify that by re-derivation outside the const fn.
        for (case, row) in TRI_TABLE.iter().enumerate() {
            let runtime = row.chunks(3).take_while(|c| c[0] != -1).count() as u8;
            assert_eq!(
                TRI_COUNT[case], runtime,
                "case {case}: const TRI_COUNT={} runtime={}",
                TRI_COUNT[case], runtime,
            );
            // After the terminator the rest of the row must also be -1.
            let used = (TRI_COUNT[case] as usize) * 3;
            for &v in &row[used..] {
                assert_eq!(v, -1, "case {case}: trailing non-(-1) past triangle list");
            }
        }
    }

    #[test]
    fn tri_table_edges_are_valid() {
        // Every non-terminator edge ID must be in 0..12.
        for (case, row) in TRI_TABLE.iter().enumerate() {
            for &v in row.iter() {
                if v == -1 {
                    continue;
                }
                assert!((0..12).contains(&v), "case {case}: invalid edge id {v}");
            }
        }
    }

    #[test]
    fn edge_table_matches_triangles() {
        // For every case, EDGE_TABLE bit `e` set iff edge `e` appears in
        // TRI_TABLE for that case. A vertex must be interpolated for an edge
        // ⇔ that edge is referenced by at least one triangle.
        for (case, row) in TRI_TABLE.iter().enumerate() {
            let mut needed: u16 = 0;
            for &v in row.iter() {
                if v == -1 {
                    continue;
                }
                needed |= 1u16 << (v as u32);
            }
            assert_eq!(
                EDGE_TABLE[case] & 0xfff,
                needed,
                "case {case}: edge_table=0x{:03x} but tri_table needs 0x{:03x}",
                EDGE_TABLE[case] & 0xfff,
                needed,
            );
        }
    }

    #[test]
    fn edge_vertices_endpoint_axes_are_unit() {
        // Every cube edge connects two corners that differ in exactly one
        // axis by 1. Sanity-check both corner numbering and EDGE_VERTICES.
        for (e, &[a, b]) in EDGE_VERTICES.iter().enumerate() {
            let ca = CORNER_OFFSETS[a as usize];
            let cb = CORNER_OFFSETS[b as usize];
            let diff = [
                (ca[0] as i32 - cb[0] as i32).abs(),
                (ca[1] as i32 - cb[1] as i32).abs(),
                (ca[2] as i32 - cb[2] as i32).abs(),
            ];
            let total: i32 = diff.iter().sum();
            assert_eq!(
                total, 1,
                "edge {e}: corners {a}->{b} differ on {} axes (expected 1)",
                total
            );
        }
    }

    #[test]
    fn pack_for_gpu_section_offsets_are_consistent() {
        let packed = pack_for_gpu();
        assert_eq!(packed.len(), GpuMcTablesLayout::TOTAL_WORDS);

        // Spot-check the boundary entries.
        assert_eq!(
            packed[GpuMcTablesLayout::EDGE_TABLE_OFFSET],
            EDGE_TABLE[0] as u32
        );
        assert_eq!(
            packed[GpuMcTablesLayout::EDGE_TABLE_OFFSET + 255],
            EDGE_TABLE[255] as u32
        );
        // First entry of TRI_TABLE
        assert_eq!(
            packed[GpuMcTablesLayout::TRI_TABLE_OFFSET] as i32 as i8,
            TRI_TABLE[0][0]
        );
        // Edge vertices first pair
        assert_eq!(
            packed[GpuMcTablesLayout::EDGE_VERTICES_OFFSET],
            EDGE_VERTICES[0][0]
        );
        // Tri count last
        assert_eq!(
            packed[GpuMcTablesLayout::TRI_COUNT_OFFSET + 255],
            TRI_COUNT[255] as u32
        );
    }

    #[test]
    fn pack_for_gpu_negative_terminators_round_trip() {
        // -1 i8 must survive the i32-as-u32 reinterpretation (= 0xFFFFFFFF).
        let packed = pack_for_gpu();
        let last_tri_word =
            packed[GpuMcTablesLayout::TRI_TABLE_OFFSET + GpuMcTablesLayout::TRI_TABLE_WORDS - 1];
        assert_eq!(last_tri_word, 0xFFFF_FFFF);
        assert_eq!(last_tri_word as i32, -1);
    }

