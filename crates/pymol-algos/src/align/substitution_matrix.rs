    fn test_case_insensitive() {
        assert_eq!(BLOSUM62.score('a', 'A'), BLOSUM62.score('A', 'A'));
        assert_eq!(BLOSUM62.score('w', 'y'), BLOSUM62.score('W', 'Y'));
    }

