//! Substitution matrices for sequence alignment (BLOSUM, PAM, etc.)
//!
//! All data is stored as static arrays — no heap allocation or file I/O.

/// A substitution scoring matrix for sequence alignment
#[derive(Debug, Clone)]
pub struct SubstitutionMatrix {
    /// Matrix name
    pub name: &'static str,
    /// Amino acid order (maps index → single-letter code)
    order: &'static [u8],
    /// Scores as flat array (order.len() × order.len()), row-major
    scores: &'static [i8],
    /// Score for pairs involving unknown residues
    pub unknown_score: f32,
}

impl SubstitutionMatrix {
    /// Look up score for a pair of residues (single-letter codes)
    pub fn score(&self, a: char, b: char) -> f32 {
        match (self.index(a), self.index(b)) {
            (Some(i), Some(j)) => self.scores[i * self.order.len() + j] as f32,
            _ => self.unknown_score,
        }
    }

    /// Get index of a residue in the matrix, or None if not found
    fn index(&self, residue: char) -> Option<usize> {
        let upper = (residue as u8).to_ascii_uppercase();
        self.order.iter().position(|&c| c == upper)
    }
}

